#include<iostream>
using namespace std;
int main()
{
	//OPERATOR PERINTAH
	
	int bilangan=10;
	//kita memerintahkan untuk memberi nilai pada variabel
	
	bilangan+= 10; //sama dengan bilangan= bilangan+ 10;
	cout<<"bilangan +10 = "<<bilangan<<endl;
	
	bilangan-= 10; //sama dengan bilangan= bilangan+ 10;
	cout<<"bilangan -10 = "<<bilangan<<endl;
	
	bilangan*= 10; //sama dengan bilangan= bilangan+ 10;
	cout<<"bilangan * 10 = "<<bilangan<<endl;
	
	bilangan/= 10; //sama dengan bilangan= bilangan+ 10;
	cout<<"bilangan / 10 = "<<bilangan<<endl;
	
	bilangan%= 10; //sama dengan bilangan= bilangan+ 10;
	cout<<"bilangan  % 10 = "<<bilangan<<endl;
	
return 0;
	
}
